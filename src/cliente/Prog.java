            buf.add(new ConIns(XBeeWiFiControl.getDevice(jComboBox6.getSelectedIndex()).getAddr64().toString(), XBeeWiFiControl.getDevice(jComboBox7.getSelectedIndex()).getAddr64().toString(), jComboBox1.getSelectedIndex(), jComboBox3.getSelectedItem().toString(), jComboBox2.getSelectedItem().toString(), s, rHigh.isSelected(), global));
            
