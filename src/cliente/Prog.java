    private void fillTable(){
        if(Exec.getIns() != null){
            for(Ins p : Exec.getIns()){
                if(p instanceof ConIns){
                    ConIns in = (ConIns) p;
                    dtm.addRow(new Object[] {(in.isGlobal()?"[G]":"")+"Se porta "+in.getPortIn()+" em ["+XBeeWiFiControl.getDevice(in.getReadDevice()).getName()+"] "+in.getOprd()+" "+in.getConV()+" então porta "+in.getPortOut()+" em ["+XBeeWiFiControl.getDevice(in.getWriteDevice()).getName()+"]  -> "+(in.getSetV() ? "HIGH" : "LOW")});
                } else {
                    if(p instanceof LogIns){
                        LogIns li = (LogIns) p;
                        dtm.addRow(new Object[] {(li.isGlobal()?"[G]":"")+"Log da porta em ["+XBeeWiFiControl.getDevice(li.getAddr()).getName()+"] "+li.getPort()+" no arquivo "+li.getNomeArq()});
                    } else {
                        Delay dl = (Delay) p;
                        dtm.addRow(new Object[] {"Delay de "+ dl.getTime() +"ms em ["+XBeeWiFiControl.getDevice(dl.getAddr()).getName()+"]"});
                    }
                }
            }
        }
    } 

